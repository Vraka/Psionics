<h2>Overview</h2>
<p>The Voidlings are beings made from sentient void energy. They can be summoned from the Void by those with a strong enough Void affinity.</p>
<img src="../img/voidling.png">
<h2>Appearance</h2>
<p>As conglomerates of energy, Voidlings are polymorphic, and have no stable shape. When summoned, they are roughtly humanoid, but can take on any form. However, they will always retain the same black or dark purple color of the energy.</p>
<h2>Abilities</h2>
<p>While they have no explicit abilities outside of the natural properties of void energy, Voidlings can merge with each other to become stronger. However, the Voidlings individual strength depends on the one who summons them.</p>
