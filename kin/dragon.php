<h2>Overview</h2>
<p>Dragons, or draconics</p>

<h2>Energy</h2>
<p>Due to their connection to their animal, therians exhibit a strong affinity to the earth element. Of course, this doesn't mean they won't have any other affinities, or even that earth would be their dominant one.</p>

<h2>Therian Types</h2>
<p>Therian types or theriotypes can be any animal that once or still exists on Earth. The most common types are canines or felines, but the animal identities are absolutely not limited to them.</p>
<p>Therians can also exhibit different degrees of shifting:</p>
<h3>Shifter Therian</h3>
<p>The "typical" therian, one who can shift between fully human and fully animal.</p>
<h3>Contherian</h3>
<p>A therian who cannot shift, and is permanently in a state of half human/half animal</p>
<h3>Vacillant Therian</h3>
<p>A therian who has more limited ability in shifting, and has the instincts of their animal ingraved in their personality.</p>
<h3>Cladotherian</h3>
<p>A type of therian who relates to a entire family of animals instead of a single species.</p>
<h3>Polytherian</h3>
<p>A therian who identifies as more than one animal. (Also generalized as a Shifter)</p>

<h2>Shifting Types</h2>
<p>As said above, therians can go through various kinds of shifting.</p>
<h3>Phantom Shifting</h3>
<p>Phantom shifting can occur in both the physical and etheric, where the person feels body parts corresponding to their animal body (phantom limbs) added or overlaying to their physical body.</p>
<h3>Mental Shifting</h3>
<p>Mental shifting can also occure in both the physical and the etheric, where the person takes on part of all of the mentality of their theriotype.</p>
<h3>Dream/Astral Shifting</h3>
<p>Not a significant shift, but therians can take on the form of their theriotype in the dreams/astral plane.</p>
<h3>Full Shifting</h3>
<p>Full shifting or just shifting, occurs only in the etheric plane. This is when the therian fully takes on the form of their theriotype.</p>
<h3>Physical Shifting</h3>
<p>Physical shifting is the idea that a therian can experience a full shift in the physical plane. This is generally thought of as impossible, considering how hard it is to express any form of energy work in the physical plane. There is yet to be any proof that physical shifting is possible.</p>
