<h1>Kitsune</h1>
<h2>Overview</h2>
<h4><p>The kitsune are a race of fox yokai of Shinto origin. Created by the fox goddess Inari, the kitsune are mischievous and ellusive. While they love to play tricks on others, they hide away their villages away from prying eyes. Kitsune are rarely seen in their true form, if at all.</p></h4>
<br>
<h2>Appearance</h2>
<h4><p>As polymorphic beings, the kitsune can take on virtually any form they choose. However, their base form is that of a multi-tailed fox, with lighter fur colors. They are ususally thin and lean, as kitsune are primarily speed fighters as opposed to brute strength. In fact, their tails are the strongest limbs and can be used to attack, move, or pick up objects.</p></h4>
<br>
<h2>Abilities</h2>
<h4><p>Kitsune can have anywhere between one and nine tails, each tail representing a level of mastery over one of their nine abilities: Shapeshifting, Fox Fire, Pocket Realm (PR) Creation, Illusion, Main Affinity Control, 4 Basic Element Control, Seduction, Possession, and Dreamwalking. While the first tail a kitsune is born with is usually Shapeshifting, the order in which the subsequent tails are earned depends on which ability the kitsune masters next.</p></h4>
<h3><p>Shapeshifting</p></h3>
<h4><p>Generally the first ability a kitsune has when they are born, kitsune are able to polymorph into virtually anything. In order to achieve more complex shifts, for example gills to filter oxygen out of water, kitsune need to have a clear idea of not only how their desired outcome looks, but also how it functions on a base level. Additionally, while simple shifts take negligible energy to hold indefinitely, complex shifting and especially large changes in mass require more energy, and are much harder to maintain.</p></h4>
