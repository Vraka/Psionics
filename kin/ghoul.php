<h1>Ghoul</h1>
<div class="center box">
<h2>Overview</h2>
<p>Ghouls are beings that have multiple unstable cores in one body. They are either created intentionally or by accident, through tampering with a core. Mindless beings, they attack others and consume more cores to sustain themselves.</p>
<img src="../img/ghoul.png" width=30% height=auto>
<h2>Appearance</h2>
<p>While vaguely resembling the original being the ghoul was made from, their bodies become erroded with a tar-like substance. They can also look like large tar versions of animal.</p>
<h2>Bite</h2>
<p>Ghouls give off a negative corruptive energy making any wound inflicted by one much harder to heal. They latch onto a target, biting them and consuming their flesh and energy to reach their core. However, if left unchecked, their energy could cause the ghoul transformation in the victim.</p>
<h2>Cure</h2>
<p>It is possible to separate the souls in a newly created ghoul, essentially saving the souls. However, this is itself a difficult process, and the timeframe for this method to be successful is very short. After that, the only thing to do is kill the ghoul, sending the souls inside it to be reborn.</p>
<h2>Mutation</h2>
<p>Once a ghoul consumes enough souls, it can undergo three stages of mutation:</p>
<h3>Demi</h3>
<p>A Demi ghouls is still mindless, but takes on a somewhat humanoid shape.</p>
<h3>Demi Magnus</h3>
<p>Once a Demi Magnus, the ghoul becomes more self-aware and can use energy.</p>
<h3>Altissimus</h3>
<p>The final state of mutation, the ghoul becomes one soul, using the others to push its evolution. The form it takes is random, so no alti will look the same.</p>
<h2>Awakened</h2>
<p>There are exceedingly rare instances of ghouls either retaining or regaining a semblance of conciousness. They are labeled "awakened" ghouls, however they still need to sustain themselves with the cores of others.</p>
<h2>Locations</h2>
<p>There is currently a large population of ghouls in Northern Russia and Northern China.</p>
