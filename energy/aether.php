<h1>Aether (Energy)</h1>
<div class="center box">
<h2>Overview</h2>
<p>Aether or Life energy is considered the opposite of Death energy, encompassing the 'life force' of all living things. However, this doesn't mean the energy is found in every living being. This affinity is in itself life; it can make things grow faster, heal faster, etc.</p>
<h2>Properties</h2>
Aether energy naturally tends towards the giving of life - causing and directing plant growth, healing, and (in extreme cases) reviving the recently dead. Etheric plants can be fully grown in a matter of minutes. Aether is more of a defensive affinity, it also can increase regeneration. There have been cases where people have used the element offensively to corrupt another’s energy system, or leach life from that person they are fighting.
<h2>Strains</h2>
<h4>Prometheus’s Aether</h4>
<p>Prometheus’s strain of Aether is highly passive and radiates throughout the energy system and aura of the user; it helps speed up the growth of nature around the user with every step they take.</p>
