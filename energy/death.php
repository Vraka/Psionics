<h1>Death (Energy)</h1>
<div class="center box">
<h2>Overview</h2>
<p>Considered the opposite of Aether energy, the Death element revolves around decay and the end of life. Death energy is released when someone or something dies. Normally only a small amount of energy is released and dissipates within minutes. Gaining death as an affinity is really only down two ways; first you can be born with it: some races such as reapers or banshees for example are born with death. Or you can seek out a death deity and ask them to give you their strain of death.</p>
<h2>Properties</h2>
<p>Death energy is naturally aligned with the taking of life, causing decay and rot to the world around it. While considered one of the corruptive energies, that doesn't mean it itself is 'evil' or anything of the sort. Death is a natural phenomenon, present everywhere there is life, it is a natural part of the world around us. That said, those exposed to the Death element who are not attuned to it should flush it out of their ES as soon as possible, seeking help if needed. Otherwise, its natural tendency is to eat away at your body and life, rotting you from the inside out.</p>
<h2>Strains</h2>
<h4>Macha’s Death</h4>
<p>Macha’s strain is intimidating, and infectious; one small cut or stab and it spreads, rotting and corrupting energy matching that energies natural flow.</p>
