<h1>Darkness (Energy)</h1>
<div class="center box">
<h2>Overview</h2>
<p>Considered the antithesis of the Light element, Darkness energy is aligned with shadows and the absence of Light.</p>
<h2>Properties</h2>
<p>Darkness is commonly used as an offensive element, as well as in concealment and curses, manipulating shadows to hide that which doesn't want to be found. It has uses in stealth, shadow travel, and impairing the senses of others, and a variety of strong corruptive attacks. While considered a corruptive element, it's a misconception that Darkness as a whole attacks all Light. This only applies to Nox's/Nyx's strain or flavor of darkness.</p>
<h2>Strains</h2>
<h4>Erebus's Darkness</h4>
<p>Erebus’s strain of darkness has corruptive qualities but at a much less degree than that of Nyx’s, Like her strain Erebus’s strain contains its own sentience. Unlike Nyx’s strain it does not affect mental or emotional states. However this darkness can be often confused with fire or heated elements when over looked as it has a much higher temperature then any other strain of darkness.</p>
<h4>Kauket's Darkness</h4>
<p>Kauket’s strain has ben described as calm or like a nights sky; it is often seen as being strongly aligned with lunar energy. Kuk’s strain is almost identical to Kauket’s.</p>
<h4>Kingu's Darkness</h4>
<p>Kingu’s darkness has a high acidic and poisonous nature, often leading to toxic damage to others energy systems or melting of flesh and bone when exposed to it. As Tiamat’s son his darkness carries very similar prosperities to her Void.</p>
<h4>Kuk's Darkness</h4>
<p>Kuk’s strain of darkness much like Kauket’s strain has been described as being calm like the nights sky; often being seen as strongly aligned with lunar energy.</p>
<h4>Nox's/Nyx's Darkness</h4>
<p>Nyx’s darkness is highly corruptive; due to her strain having its own sentience it has a tendency too corrupt others even when not willed by the user; it is particularly painful for light beings as it has an accelerated rate of corruption with them. This strain of darkness commonly matches Nyx’s mental state and will start to manipulate those with the strain into thinking more aligned with her thoughts.</p>

