<h1>Solar (Energy)</h1>
<div class="center box">
<h2>Overview</h2>
<p>Solar is an element stars (as the sun is a star) tend to all contain. But the energy strains do not directly come from the stars themselves but rather from Solar Primordial’s or deities.</p>
<h2>Properties</h2>
<p>Much like the sun, the Solar element's defining characteristic is it's heat, and has the potential reach the temperature of a super nova. To those not attuned to the element, Solar energy will remain very hot, even when lessened to a bearable degree.</p>
<h2>Strains</h2>
<h4>Shamash’s Solar</h4>
<p>Shamash’s strain burns hot but is bearable when not pushed to higher limits. Shamash’s strain has its own sentience however it has not been seen or proven as being hostile to other energies.</p>
<h4>Ra’s Solar</h4>
<p>Ra’s strain has a rather strong intensity to it, often causing people to feel as tho they are walking through a desert.</p>
<h4>Nergal’s Solar</h4>
<p>Unlike other solar deities Nergal’s energy is seen as a black sun; it is seen as being heavily mixed with darkness and is often linked to pestilence. Nergal’s strain is the polar opposite to Samas’s strain.</p>
<h4>Sol’s Solar</h4>
<p>Sol’s strain unlike the other feels more like a primal heat; where it’s based on instinct rather than intent or will. It reacts like an animal would towards people, “attacking” (burning at high temperatures) when threatened/feeling unsafe or “pampers” (warms and comforts) when around allies/friends/safety/etc.</p>
