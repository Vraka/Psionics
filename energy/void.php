<h1>Void (Energy)</h1>
<div class="center box">
<h2>Overview</h2>
<p>Void energy was the predominant energy of the universe before creation, and housed the cosmic energy from which the universe was born.</p>
<h2>Properties</h2>
An extremely dangerous and misunderstood element, Void energy contains all affinities, and can be changed into each one with proper training. It naturally consumes any and all energy it comes into contact with, allowing it to be used as a general energy nullifier . Unlike corruption, Void energy has no malignant intent behind this; it's a natural process. Left unchecked, Void will indefinitely consume all energy that comes into close proximity.
<h2>Chaos</h2>
Generally speaking, Chaos is another term for Void energy, a reaction to the use of Void. Chaos is seen as formless matter that existed prior to the creation of the universe. When something happens within the nothingness of the Void - through the active use of the Void element - it is labeled as Chaos. Due to Cosmic existing before creation, Chaos may have a relationship with Cosmic as well.
