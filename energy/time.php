<h2>Overview</h2>
<p>Time energy is exactly what it sounds like - energy concerned with the flow of time.</p>
<h2>Properties</h2>
<p>Accelerating, stopping, and reversing the flow of time are all possible through the use of the Time element. The energy necessary increases proportionally to the amount of space affected. As such, extending the effects to the entire world is impossible for any individual (with very few possible exceptions). In essence, the range of your ability depends on the capacity of your ES and your practice with the element. That said, it's easier to utilize Time energy to affect the entirety of space you own or created, like a PR. Looking on a larger scale, this is why realms like Cyradin and Djinnestan have a faster time flow than the rest of the etheric. I would also tentatively hypothesize that the element would make clarvoyant future-sight easier, but I have yet to obtain evidence on either side of that topic.</p>
