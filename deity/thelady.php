<div class="center box">
<h2>Overview</h2>
<p>The Lady is a kitsune that is mentioned in myth. However, what is written about her is not totally accurate. The Lady, like some of the Shinto myths is considered a overall mother figure to the kitsune race. However, unlike some versions of the myth, she is a separate entity from the goddess Inari. The Lady is the caretaker and protector of the race of kistune that follow Inari ESPECIALLY when Inari is not present. As for appearance, The Lady has pure white fur and usually appears to have 10 tails (1000 in an alternate form). The lady tends to wander but is currently residing in a Shinto Pocket Realm that is owned by Amaterasu.</p>
